/// <summary>
/// Specifies the number of dimensions for a neural network module.
/// </summary>
public enum Dimensions
{
    /// <summary>
    /// No dimensions.
    /// </summary>
    None = 0,

    /// <summary>
    /// One dimension.
    /// </summary>
    One = 1,

    /// <summary>
    /// Two dimensions.
    /// </summary>
    Two = 2,

    /// <summary>
    /// Three dimensions.
    /// </summary>
    Three = 3
}